%% demo_fuselage_curves_xyz_rewritten.m
% Curve-only fuselage demo using explicit XYZ point sets.
% No local functions; all geometry is defined directly in this script.
% All curves are created with the geom curve toolbox.
%
% Run from repo root:
%   demo_fuselage_curves_xyz_rewritten

clear; close all; clc;

%% ------------------------------------------------------------------------
% User settings
%% ------------------------------------------------------------------------
pCurve     = 3;
fitMethod  = 'centripetal';
curveNSamp = 300;

%% ------------------------------------------------------------------------
% Path / package check
%% ------------------------------------------------------------------------
script_dir = fileparts(mfilename('fullpath'));
if ~isempty(script_dir)
    addpath(script_dir);
end
if ~exist('+geom', 'dir') && isempty(which('geom.NURBSCurve'))
    error(['Cannot find +geom package.\n' ...
           'Run this from the repo root or add the geometry kernel to path.']);
end
fprintf('geom package found at: %s\n', script_dir);

%% ------------------------------------------------------------------------
% 1) Define longitudinal fuselage profile points directly in XYZ
%    All of these lie on the symmetry plane y = 0.
%% ------------------------------------------------------------------------
fprintf('\n--- 1. Build longitudinal profile curves from XYZ points ---\n');

% Top profile: nose -> tail
P_top = [ ...
    0.00, 0.00,  0.00; ...
    0.20, 0.00,  0.10; ...
    0.40, 0.00,  0.33; ...
    1.00, 0.00,  0.75; ...
    2.00, 0.00,  1.03; ...
    4.00, 0.00,  1.10; ...
    6.50, 0.00,  1.10; ...
    8.50, 0.00,  1.08; ...
   10.00, 0.00,  0.90; ...
   11.20, 0.00,  0.46; ...
   11.70, 0.00,  0.18; ...
   12.00, 0.00,  0.00];

% Bottom profile: nose -> tail
P_bot = [ ...
    0.00, 0.00,  0.00; ...
    0.20, 0.00, -0.06; ...
    0.40, 0.00, -0.27; ...
    1.00, 0.00, -0.53; ...
    2.00, 0.00, -0.80; ...
    4.00, 0.00, -1.10; ...
    6.50, 0.00, -1.10; ...
    8.50, 0.00, -1.00; ...
   10.00, 0.00, -0.72; ...
   11.20, 0.00, -0.30; ...
   11.70, 0.00, -0.10; ...
   12.00, 0.00,  0.00];

% Center / widest-side profile: nose -> tail
P_ctr = [ ...
    0.00, 0.00,  0.00; ...
    0.20, 0.05,  0.00; ...
    0.40, 0.24,  0.02; ...
    1.00, 0.64,  0.10; ...
    2.00, 0.99,  0.12; ...
    4.00, 1.10,  0.00; ...
    6.50, 1.10,  0.00; ...
    8.50, 1.08,  0.04; ...
   10.00, 0.84,  0.08; ...
   11.20, 0.37,  0.06; ...
   11.70, 0.12,  0.02; ...
   12.00, 0.00,  0.00];

Ctop = geom.NURBSCurve.globalInterp(P_top, pCurve, fitMethod);
Cbot = geom.NURBSCurve.globalInterp(P_bot, pCurve, fitMethod);
Cctr = geom.NURBSCurve.globalInterp(P_ctr, pCurve, fitMethod);

fprintf(' built top, bottom, and center profile curves\n');

%% ------------------------------------------------------------------------
% 2) Define fuselage section curves directly in XYZ
%    Each section is split into an upper and lower half-curve.
%    Edit these points directly to shape the fuselage.
%% ------------------------------------------------------------------------
fprintf('\n--- 2. Build fuselage section curves from XYZ points ---\n');

% Section at x = 0.40
Pup{1} = [ ...
    0.40, 0.00, 0.33; ...
    0.40, 0.04, 0.33; ...
    0.40, 0.12, 0.31; ...
    0.40, 0.20, 0.25; ...
    0.40, 0.24, 0.10; ...
    0.40, 0.24, 0.02];
Plo{1} = [ ...
    0.40, 0.00, -0.27; ...
    0.40, 0.04, -0.27; ...
    0.40, 0.12, -0.25; ...
    0.40, 0.20, -0.18; ...
    0.40, 0.24, -0.05; ...
    0.40, 0.24,  0.02];

% Section at x = 1.00
Pup{2} = [ ...
    1.00, 0.00, 0.75; ...
    1.00, 0.10, 0.75; ...
    1.00, 0.28, 0.71; ...
    1.00, 0.48, 0.55; ...
    1.00, 0.60, 0.28; ...
    1.00, 0.64, 0.10];
Plo{2} = [ ...
    1.00, 0.00, -0.53; ...
    1.00, 0.10, -0.53; ...
    1.00, 0.28, -0.48; ...
    1.00, 0.48, -0.32; ...
    1.00, 0.60, -0.06; ...
    1.00, 0.64,  0.10];

% Section at x = 2.00
Pup{3} = [ ...
    2.00, 0.00, 1.03; ...
    2.00, 0.14, 1.03; ...
    2.00, 0.42, 0.99; ...
    2.00, 0.72, 0.78; ...
    2.00, 0.92, 0.38; ...
    2.00, 0.99, 0.12];
Plo{3} = [ ...
    2.00, 0.00, -0.80; ...
    2.00, 0.14, -0.80; ...
    2.00, 0.42, -0.73; ...
    2.00, 0.72, -0.50; ...
    2.00, 0.92, -0.10; ...
    2.00, 0.99,  0.12];

% Section at x = 4.00
Pup{4} = [ ...
    4.00, 0.00, 1.10; ...
    4.00, 0.16, 1.10; ...
    4.00, 0.48, 1.06; ...
    4.00, 0.82, 0.84; ...
    4.00, 1.02, 0.34; ...
    4.00, 1.10, 0.00];
Plo{4} = [ ...
    4.00, 0.00, -1.10; ...
    4.00, 0.16, -1.10; ...
    4.00, 0.48, -1.02; ...
    4.00, 0.82, -0.78; ...
    4.00, 1.02, -0.28; ...
    4.00, 1.10,  0.00];

% Section at x = 6.50
Pup{5} = [ ...
    6.50, 0.00, 1.10; ...
    6.50, 0.16, 1.10; ...
    6.50, 0.48, 1.06; ...
    6.50, 0.82, 0.84; ...
    6.50, 1.02, 0.34; ...
    6.50, 1.10, 0.00];
Plo{5} = [ ...
    6.50, 0.00, -1.10; ...
    6.50, 0.16, -1.10; ...
    6.50, 0.48, -1.02; ...
    6.50, 0.82, -0.78; ...
    6.50, 1.02, -0.28; ...
    6.50, 1.10,  0.00];

% Section at x = 8.50
Pup{6} = [ ...
    8.50, 0.00, 1.08; ...
    8.50, 0.15, 1.08; ...
    8.50, 0.46, 1.03; ...
    8.50, 0.78, 0.80; ...
    8.50, 0.98, 0.30; ...
    8.50, 1.08, 0.04];
Plo{6} = [ ...
    8.50, 0.00, -1.00; ...
    8.50, 0.15, -1.00; ...
    8.50, 0.46, -0.94; ...
    8.50, 0.78, -0.70; ...
    8.50, 0.98, -0.20; ...
    8.50, 1.08,  0.04];

% Section at x = 10.00
Pup{7} = [ ...
   10.00, 0.00, 0.90; ...
   10.00, 0.12, 0.90; ...
   10.00, 0.34, 0.86; ...
   10.00, 0.60, 0.66; ...
   10.00, 0.78, 0.26; ...
   10.00, 0.84, 0.08];
Plo{7} = [ ...
   10.00, 0.00, -0.72; ...
   10.00, 0.12, -0.72; ...
   10.00, 0.34, -0.66; ...
   10.00, 0.60, -0.48; ...
   10.00, 0.78, -0.08; ...
   10.00, 0.84,  0.08];

% Section at x = 11.20
Pup{8} = [ ...
   11.20, 0.00, 0.46; ...
   11.20, 0.05, 0.46; ...
   11.20, 0.14, 0.43; ...
   11.20, 0.26, 0.30; ...
   11.20, 0.34, 0.12; ...
   11.20, 0.37, 0.06];
Plo{8} = [ ...
   11.20, 0.00, -0.30; ...
   11.20, 0.05, -0.30; ...
   11.20, 0.14, -0.27; ...
   11.20, 0.26, -0.14; ...
   11.20, 0.34,  0.00; ...
   11.20, 0.37,  0.06];

nSec = numel(Pup);
Cup = cell(1, nSec);
Clo = cell(1, nSec);

for k = 1:nSec
    Cup{k} = geom.NURBSCurve.globalInterp(Pup{k}, pCurve, fitMethod);
    Clo{k} = geom.NURBSCurve.globalInterp(Plo{k}, pCurve, fitMethod);

    xsec = Pup{k}(1,1);
    yhw  = max([Pup{k}(:,2); Plo{k}(:,2)]);
    ztop = max(Pup{k}(:,3));
    zbot = min(Plo{k}(:,3));
    fprintf(' section %2d: x = %6.3f, half-width = %.4f, ztop = %.4f, zbot = %.4f\n', ...
        k, xsec, yhw, ztop, zbot);
end

%% ------------------------------------------------------------------------
% 3) Plot 3D curves and input points
%% ------------------------------------------------------------------------
fprintf('\n--- 3. Plot fuselage-defining curves ---\n');

figure('Name','Fuselage defining curves (3D)');
hold on; grid on; axis equal; view(3);

% Raw longitudinal points
plot3(P_top(:,1), P_top(:,2), P_top(:,3), 'o-', 'LineWidth', 1.0, 'MarkerSize', 5);
plot3(P_bot(:,1), P_bot(:,2), P_bot(:,3), 'o-', 'LineWidth', 1.0, 'MarkerSize', 5);
plot3(P_ctr(:,1), P_ctr(:,2), P_ctr(:,3), 'o-', 'LineWidth', 1.0, 'MarkerSize', 5);

% Fitted longitudinal curves
Ctop.plot(curveNSamp, 'Color', [0.85 0.15 0.15], 'LineWidth', 2.0, 'ShowCP', false, 'ShowKnots', false);
Cbot.plot(curveNSamp, 'Color', [0.15 0.25 0.85], 'LineWidth', 2.0, 'ShowCP', false, 'ShowKnots', false);
Cctr.plot(curveNSamp, 'Color', [0.15 0.60 0.20], 'LineWidth', 2.0, 'ShowCP', false, 'ShowKnots', false);

% Raw section points + fitted section curves
for k = 1:nSec
    plot3(Pup{k}(:,1), Pup{k}(:,2), Pup{k}(:,3), 'k.-', 'LineWidth', 0.8, 'MarkerSize', 10);
    plot3(Plo{k}(:,1), Plo{k}(:,2), Plo{k}(:,3), 'k.-', 'LineWidth', 0.8, 'MarkerSize', 10);

    Cup{k}.plot(curveNSamp, 'Color', [0.80 0.30 0.10], 'LineWidth', 1.5, 'ShowCP', false, 'ShowKnots', false);
    Clo{k}.plot(curveNSamp, 'Color', [0.10 0.35 0.80], 'LineWidth', 1.5, 'ShowCP', false, 'ShowKnots', false);
end

% Symmetry line for reference
plot3([0 12], [0 0], [0 0], 'k--', 'LineWidth', 1.0);

xlabel('X'); ylabel('Y'); zlabel('Z');
title('Half-fuselage defining curves from explicit XYZ points');
legend({'Top pts','Bottom pts','Center pts', ...
        'Top curve','Bottom curve','Center curve'}, ...
        'Location','best');

%% ------------------------------------------------------------------------
% 4) Plot section shapes in Y-Z for easier editing
%% ------------------------------------------------------------------------
figure('Name','Fuselage sections (Y-Z view)');
hold on; grid on; axis equal;

for k = 1:nSec
    plot(Pup{k}(:,2), Pup{k}(:,3), 'k.-', 'LineWidth', 0.8, 'MarkerSize', 10);
    plot(Plo{k}(:,2), Plo{k}(:,3), 'k.-', 'LineWidth', 0.8, 'MarkerSize', 10);

    uUp = linspace(Cup{k}.domain(1), Cup{k}.domain(2), curveNSamp);
    uLo = linspace(Clo{k}.domain(1), Clo{k}.domain(2), curveNSamp);
    Qup = Cup{k}.evaluate(uUp);
    Qlo = Clo{k}.evaluate(uLo);

    plot(Qup(:,2), Qup(:,3), 'LineWidth', 1.5);
    plot(Qlo(:,2), Qlo(:,3), 'LineWidth', 1.5);
end

plot([0 0], ylim, 'k--', 'LineWidth', 1.0);
xlabel('Y'); ylabel('Z');
title('Half-section curves in Y-Z plane');

%% ------------------------------------------------------------------------
% 5) Endpoint tangent check at the symmetry plane
%% ------------------------------------------------------------------------
fprintf('\n--- 4. Endpoint tangent check at y = 0 ---\n');
for k = 1:nSec
    dUp0 = Cup{k}.derivative(Cup{k}.domain(1), 1);
    dLo0 = Clo{k}.derivative(Clo{k}.domain(1), 1);
    fprintf(' section %2d upper start tangent = [% .4e % .4e % .4e]\n', k, dUp0(1), dUp0(2), dUp0(3));
    fprintf(' section %2d lower start tangent = [% .4e % .4e % .4e]\n', k, dLo0(1), dLo0(2), dLo0(3));
end

fprintf('\nDone.\n');
fprintf('Edit the XYZ point arrays near the top of this script to change the body shape.\n');
fprintf('All curves are built directly with geom.NURBSCurve.globalInterp(...).\n');
